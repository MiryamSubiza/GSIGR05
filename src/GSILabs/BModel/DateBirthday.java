public class DateBirthday {
    
    public int day; //1 <= day <= 31
    public int month; //1 <= month <= 12
    public int year; //1900 <= year <= 2015
    
    public DateBirthday (int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }
    
    public void setDay (int day) {
        this.day = day;
    }
    
    public int getDay () {
        return day;
    }
    
    public void setMonth (int month) {
        this.month = month;
    }
    
    public int getMonth () {
        return month;
    }
    
    public void setYear (int year) {
        this.year = year;
    }
    
    public int getYear () {
        return year;
    }
    
}
